// JavaScript arrays

// Define an array of fruits
const fruits = ['apple', 'banana', 'cherry', 'mango'];

// Define an image array
const images = ['mountain.jpg', 'fashion-girl.jpg', 'kitty.jpg'];

// The length of an array
// How many elements does the array contain?

console.log(`Length of fruits array: ${fruits.length}`)
// Output would be:
// Length of fruits array: 4

console.log(`Length of images array: ${images.length}`)
// Output would be:
// Length of images array: 3
